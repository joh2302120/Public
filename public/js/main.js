function changeColor() {
    let color = document.body.style.color;
       if (color = "D6FFE4") {
          document.body.style.background = "#D6FFE4";
          document.body.style.text="#29001B";
        } else {
           document.body.style.background = "#29001B";
           document.body.style.text="#D6FFE4";
         }
      }